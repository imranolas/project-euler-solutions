def largest_product(series)
	products = []
	arr = series.to_s.split('').map { |i| i.to_i }
	index = 0
	while index < arr.length - 6
		products << (index..index+5).inject(1) { |sum,i| sum * arr[i] }
		index += 1
	end
	products.max
end

